END 
GO
CREATE PROC SP_EMPLEADOS_DELETE 
    @ID_EMPLEADO NUMERIC
AS 
BEGIN 
DELETE
FROM   EMPLEADO
WHERE  ID_EMPLEADO = @ID_EMPLEADO
 
END
GO


