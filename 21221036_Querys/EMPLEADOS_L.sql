IF OBJECT_ID('SP_EMPLEADOS_CREATE') IS NOT NULL
BEGIN 
DROP PROC SP_EMPLEADOS_CREATE 
END
GO
CREATE PROCEDURE SP_EMPLEADOS_CREATE
  @ID_EMPLEADO NUMERIC(12),
  @CORREO_ELECTRONICO VARCHAR(100),
  @PRIMER_NOMBRE VARCHAR(100),
  @SEGUNDO_NOMBRE VARCHAR(100),
  @PRIMER_APELLIDO VARCHAR(100),
  @SEGUNDO_APELLIDO VARCHAR(100),
  @DIRECCION VARCHAR(100),
  @FECHA_INICIO DATE,
  @FECHA_NACIMIENTO DATE,
  @CORREO_PRIMARIO VARCHAR(100),
  @CORREO_SECUNDARIO VARCHAR(100),
  @FECHA_CREACION DATE,
  @FECHA_ACTUALIZACION DATE,
  @USUARIO_CREADOR VARCHAR(100),
  @USUARIO_ACTUALIZACION VARCHAR(100)
AS
BEGIN
INSERT INTO EMPLEADO  (
    ID_EMPLEADO,
    CORREO_ELECTRONICO,
    PRIMER_NOMBRE,
    SEGUNDO_NOMBRE,
    PRIMER_APELLIDO,
    SEGUNDO_APELLIDO,
    DIRECCION,
    FECHA_INICIO,
    FECHA_NACIMIENTO,
    CORREO_PRIMARIO,
    CORREO_SECUNDARIO,
	  FECHA_CREACION,
    FECHA_ACTUALIZACION,
    USUARIO_CREADOR,
    USUARIO_ACTUALIZACION)
  VALUES (
    @ID_EMPLEADO,
    @CORREO_ELECTRONICO,
    @PRIMER_NOMBRE,
    @SEGUNDO_NOMBRE,
    @PRIMER_APELLIDO,
    @SEGUNDO_APELLIDO,
    @DIRECCION,
    @FECHA_INICIO,
    @FECHA_NACIMIENTO,
    @CORREO_PRIMARIO,
    @CORREO_SECUNDARIO,
    @FECHA_CREACION,
    @FECHA_ACTUALIZACION,
    @USUARIO_CREADOR,
    @USUARIO_ACTUALIZACION)
      
SELECT 
    @CORREO_ELECTRONICO = CORREO_ELECTRONICO,
    @PRIMER_NOMBRE = PRIMER_NOMBRE,
    @SEGUNDO_NOMBRE = SEGUNDO_NOMBRE,
    @PRIMER_APELLIDO = PRIMER_APELLIDO,
    @SEGUNDO_APELLIDO = SEGUNDO_APELLIDO,
    @DIRECCION = DIRECCION,
    @FECHA_INICIO = FECHA_INICIO,
    @FECHA_NACIMIENTO = FECHA_NACIMIENTO,
    @CORREO_PRIMARIO = CORREO_PRIMARIO,
    @CORREO_SECUNDARIO = CORREO_SECUNDARIO,
	  @FECHA_CREACION = FECHA_CREACION,
    @FECHA_ACTUALIZACION = FECHA_ACTUALIZACION,
    @USUARIO_CREADOR = USUARIO_CREADOR,
    @USUARIO_ACTUALIZACION = USUARIO_ACTUALIZACION
FROM EMPLEADO 
WHERE  ID_EMPLEADO = @ID_EMPLEADO
END


------------------------------DELETE
